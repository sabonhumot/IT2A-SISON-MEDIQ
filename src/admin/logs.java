    public void displayData() {
        try {
            connectDB con = new connectDB();
            ResultSet rs = con.getData("SELECT log_id, u_id, action, action_date, action_time FROM logs");
            DefaultTableModel model = (DefaultTableModel) (DbUtils.resultSetToTableModel(rs));
            rs.close();

            String[] column = {"Log ID", "User ID", "Action", "Date", "Time"};
            model.setColumnIdentifiers(column);

            logsTable.setModel(model);

            DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer() {
                public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                    JLabel label = new JLabel(value.toString());

                    label.setFont(new Font("OpenSans", Font.BOLD, 12));
                    label.setPreferredSize(new Dimension(label.getWidth(), 30));
                    label.setOpaque(true);
                    label.setBackground(new Color(250, 249, 246));

                    label.setForeground(Color.BLACK);
                    label.setHorizontalAlignment(SwingConstants.LEFT);
                    label.setBorder(BorderFactory.createEmptyBorder(0, 10, 0, 0));

                    return label;
                }
            };

// Apply custom header renderer to all columns
            for (int i = 0; i < logsTable.getColumnModel().getColumnCount(); i++) {
                logsTable.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            }

            DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer() {
                @Override
                public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                    JLabel label = new JLabel(value != null ? value.toString() : ""); // Ensure no null values
                    label.setFont(new Font("OpenSans", Font.PLAIN, 12));
                    label.setOpaque(true);

                    // Set alternating row colors for better visibility
                    if (row % 2 == 0) {
                        label.setBackground(new Color(245, 245, 245)); // Light gray
                    } else {
                        label.setBackground(Color.WHITE); // White
                    }

                    // Add padding inside the cell
                    label.setBorder(BorderFactory.createEmptyBorder(5, 10, 5, 10)); // Top, Left, Bottom, Right

                    // Text alignment
                    label.setHorizontalAlignment(SwingConstants.LEFT);

                    // Change text color when selected
                    if (isSelected) {
                        label.setBackground(new Color(220, 240, 255)); // Light blue for selected row
                        label.setForeground(Color.BLACK);
                    }

                    return label;
                }
            };

// Apply renderer to all columns
            for (int i = 0; i < logsTable.getColumnModel().getColumnCount(); i++) {
                logsTable.getColumnModel().getColumn(i).setCellRenderer(cellRenderer);
            }

        } catch (SQLException ex) {
            System.out.println("Errors: " + ex.getMessage());

        }

        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 6 || column == 7; // Only Edit & Delete columns are clickable
            }
        };

    }


